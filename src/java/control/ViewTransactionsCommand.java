package control;

import java.io.IOException;
import java.io.PrintWriter;
import java.sql.SQLException;
import javax.servlet.RequestDispatcher;
import javax.servlet.ServletContext;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import model.Bank;

/**
 *
 * @author G10Mini
 */
public class ViewTransactionsCommand {
    protected ServletContext context;
    protected HttpServletRequest request;
    protected HttpServletResponse response;
    
    public void init(ServletContext context, HttpServletRequest request, HttpServletResponse response) {
        this.context = context;
        this.request = request;
        this.response = response;
    }
    
    public void process(String iban) throws ServletException, IOException, SQLException, ClassNotFoundException {
        Bank bank = new Bank();
        String transferHistory = bank.getTransactions(iban);
        System.out.println(transferHistory);
        response.setContentType("string");
        PrintWriter out = response.getWriter();
        out.print(transferHistory);
        forward("");
    }
    
    protected void forward(String target) throws ServletException, IOException {
        RequestDispatcher dp = this.context.getRequestDispatcher(target);
        dp.forward(this.request, this.response);
    }
    
}
